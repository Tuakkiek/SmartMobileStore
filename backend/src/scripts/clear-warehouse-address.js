import dotenv from "dotenv";
import mongoose from "mongoose";
import path from "path";
import { fileURLToPath } from "url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
dotenv.config({ path: path.join(__dirname, "../../.env") });

import WarehouseConfiguration from "../modules/warehouse/WarehouseConfiguration.js";

const run = async () => {
  try {
    await mongoose.connect(process.env.MONGODB_CONNECTIONSTRING || process.env.MONGO_URI);
    console.log("Connected to MongoDB");

    const targetCodes = ["WH-ALL", "WH-HCM"];
    const targetAddress = "Auto-generated by product + warehouse sync migration";
    
    for (const code of targetCodes) {
      const result = await WarehouseConfiguration.updateOne(
        { warehouseCode: code, address: targetAddress },
        { $set: { address: "" } },
        { skipBranchIsolation: true }
      );
      
      console.log(`Updated ${code}: matched ${result.matchedCount}, modified ${result.modifiedCount}`);
    }

    process.exit(0);
  } catch (err) {
    console.error("Update failed", err);
    process.exit(1);
  }
};

run();
